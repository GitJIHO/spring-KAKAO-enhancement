package gift.dto;

public class CategoryRequest {

    private String name;
    private String color;
    private String imageUrl;
    private String description;

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public String getDescription() {
        return description;
    }
}
